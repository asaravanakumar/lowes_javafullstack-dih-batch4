package com.examples.java.java8.fp;

/**
 * Calculator with Objected Oriented Programming style
 */
public class CalcOOP {

    int a;
    int b;

    public int add() {
        return this.a + this.b;
    }

    public int subtract() {
        return this.a - this.b;
    }

    public int multiply() {
        return this.a * this.b;
    }

    public int divide() {
        return this.a / this.b;
    }
}
