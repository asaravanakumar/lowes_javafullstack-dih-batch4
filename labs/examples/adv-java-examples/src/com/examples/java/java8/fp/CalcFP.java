package com.examples.java.java8.fp;

/**
 * Calculator with Functional Programming style
 */
public class CalcFP {

    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int divide(int a, int b) {
        return a / b;    }
}
